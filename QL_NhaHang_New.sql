CREATE PROCEDURE PROC_REPORT_HOADON
@MAHD VARCHAR(50) 
AS
BEGIN
	SELECT nv.TENNV,hd.NGAY,hd.TONGMON,hd.TONGTIEN,ct.SOLUONG,ct.GIA,ma.TENMON,(ct.SOLUONG*ct.GIA) as THANHTIEN 
	FROM HOADON hd,CT_HOADON ct ,MON_AN ma,NHANVIEN nv 
	WHERE hd.MAHD = ct.MAHD and hd.MANV = nv.MANV and ct.MAMON = ma.MAMON and ct.MAHD = @MAHD
END


